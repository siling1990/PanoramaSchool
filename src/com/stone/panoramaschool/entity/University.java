public class University {
	private int unId;
	private int unScore;
	private String unName;
	private String unImage;
	private String unPic;
	private String unInstruction;
	public int getUnId() {
		return unId;
	}
	public void setUnId(int unId) {
		this.unId = unId;
	}
	public String getUnName() {
		return unName;
	}
	public void setUnName(String unName) {
		this.unName = unName;
	}
	public String getUnImage() {
		return unImage;
	}
	public void setUnImage(String unImage) {
		this.unImage = unImage;
	}
	
	public int getUnScore() {
		return unScore;
	}
	public void setUnScore(int unScore) {
		this.unScore = unScore;
	}
	public String getUnPic() {
		return unPic;
	}
	public void setUnPic(String unPic) {
		this.unPic = unPic;
	}
	public String getUnInstruction() {
		return unInstruction;
	}
	public void setUnInstruction(String unInstruction) {
		this.unInstruction = unInstruction;
	}
	

}
