package com.stone.panoramaschool.entity;

public class City {

private int cityId;
private String cityName;
private String cityImage;
private String cityIntroduction;
private int cityScore;
public int getCityId() {
	return cityId;
}
public void setCityId(int cityId) {
	this.cityId = cityId;
}
public String getCityName() {
	return cityName;
}
public void setCityName(String cityName) {
	this.cityName = cityName;
}
public String getCityImage() {
	return cityImage;
}
public void setCityImage(String cityImage) {
	this.cityImage = cityImage;
}
public String getCityIntroduction() {
	return cityIntroduction;
}
public void setCityIntroduction(String cityIntroduction) {
	this.cityIntroduction = cityIntroduction;
}
public int getCityScore() {
	return cityScore;
}
public void setCityScore(int cityScore) {
	this.cityScore = cityScore;
}

}
